#include "libft.h"

int		ft_strequ(char const *s1, char const *s2)
{
	int		i;
	int		res;

	res = 0;
	i = 0;
	if (s1 && s2)
	{
		while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
			i = i + 1;
		if (s1[i] - s2[i] == 0)
			return (1);
		else
			return (0);
	}
	return (0);
}
