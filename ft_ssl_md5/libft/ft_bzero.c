#include "libft.h"

void	ft_bzero(void *c, size_t n)
{
	size_t	i;
	char	*mot;

	mot = (char *)c;
	i = 0;
	while (i < n)
	{
		mot[i] = '\0';
		i = i + 1;
	}
}
