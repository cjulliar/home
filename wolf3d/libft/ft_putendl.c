#include "libft.h"

void	ft_putendl(char const *s)
{
	size_t	i;

	if (s)
	{
		i = 0;
		while (s[i] != '\0')
		{
			write(1, &s[i], 1);
			i = i + 1;
		}
		write(1, "\n", 1);
	}
}
