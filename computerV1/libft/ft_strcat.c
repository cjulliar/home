#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	i;
	char	*rep;
	size_t	y;

	rep = s1;
	i = 0;
	y = 0;
	while (s1[i] != '\0')
		i = i + 1;
	while (s2[y] != '\0')
	{
		rep[i] = s2[y];
		i = i + 1;
		y = y + 1;
	}
	rep[i] = '\0';
	return (rep);
}
