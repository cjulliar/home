#include "libft.h"

void	ft_putendl_fd(char const *s, int fd)
{
	size_t	i;

	if (s && fd)
	{
		i = 0;
		while (s[i] != '\0')
		{
			write(fd, &s[i], 1);
			i = i + 1;
		}
		write(fd, "\n", 1);
	}
}
